function getLang() {
  return localStorage.getItem("lang") || "fr";
}

function setLang(lang) {
  localStorage.setItem("lang", lang);
  applyLang(lang);
}

function applyLang(lang) {
  const dict = translations[lang] || translations.fr;
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) el.textContent = dict[key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (dict[key]) el.setAttribute("placeholder", dict[key]);
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach(el => {
    const key = el.getAttribute("data-i18n-aria-label");
    if (dict[key]) el.setAttribute("aria-label", dict[key]);
  });

  // bouton toggle
  const btn = document.getElementById("langToggle");
  if (btn) btn.textContent = (lang === "fr") ? "EN" : "FR";
}

function t(key) {
  const lang = getLang();
  const dict = translations[lang] || translations.fr;
  return dict[key] || translations.fr[key] || "";
}

function setActiveNav() {
  const path = window.location.pathname.split("/").pop() || "index.html";

  document.querySelectorAll(".nav a").forEach(a => {
    const href = a.getAttribute("href");
    if (href === path) a.classList.add("active");
  });
}

document.addEventListener("DOMContentLoaded", () => {
  const lang = getLang();
  applyLang(lang);
  setActiveNav();

  const btn = document.getElementById("langToggle");
  if (btn) {
    btn.addEventListener("click", () => {
      const current = getLang();
      setLang(current === "fr" ? "en" : "fr");
    });
  }

  const form = document.querySelector(".contact-form");
  const toast = document.getElementById("formToast");
  const toastCard = document.getElementById("formToastCard");
  const toastClose = document.getElementById("formToastClose");
  const toastTitle = document.getElementById("formToastTitle");
  const toastText = document.getElementById("formToastText");
  let hideTimer = null;

  const showToast = (title, message, isError) => {
    if (!toast || !toastCard || !toastTitle || !toastText) return;
    toastTitle.textContent = title;
    toastText.textContent = message;
    toastCard.classList.toggle("is-error", Boolean(isError));
    toast.classList.add("is-visible");

    if (hideTimer) clearTimeout(hideTimer);
    hideTimer = setTimeout(() => {
      toast.classList.remove("is-visible");
    }, 10000);
  };

  const hideToast = () => {
    if (!toast) return;
    toast.classList.remove("is-visible");
    if (hideTimer) clearTimeout(hideTimer);
  };

  if (toast) {
    toast.addEventListener("click", hideToast);
  }

  if (toastClose) {
    toastClose.addEventListener("click", (event) => {
      event.stopPropagation();
      hideToast();
    });
  }

  if (form) {
    form.addEventListener("submit", async (event) => {
      event.preventDefault();

      const action = form.getAttribute("action");
      if (!action || action === "#") {
        showToast("Envoi impossible", "Adresse d'envoi non configurée.", true);
        return;
      }

      showToast(t("toastSendingTitle"), t("toastSendingText"), false);

      try {
        const response = await fetch(action, {
          method: "POST",
          body: new FormData(form),
          headers: { Accept: "application/json" }
        });

        if (response.ok) {
          showToast(t("toastSuccessTitle"), t("toastSuccessText"), false);
          form.reset();
        } else {
          showToast(t("toastErrorTitle"), t("toastErrorText"), true);
        }
      } catch (error) {
        showToast(t("toastErrorTitle"), t("toastErrorNetworkText"), true);
      }
    });
  }
});
